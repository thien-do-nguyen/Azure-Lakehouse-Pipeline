-- =====================================================
-- 4. FACT TABLE: E-COMMERCE SALES
-- =====================================================
-- Grain:
--   One row per source order_items row.
--
-- Additive facts:
--   quantity, gross_sales_amount, discount_amount, tax_amount,
--   shipping_amount_allocated, net_sales_amount.
--
-- Degenerate dimensions:
--   order_number, source_order_id, source_order_item_id, statuses.
-- =====================================================

CREATE TABLE fact_sales (
    sales_key BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    order_date_key INTEGER NOT NULL REFERENCES dim_date(date_key),
    order_time_key INTEGER NOT NULL REFERENCES dim_time(time_key),
    customer_key BIGINT NOT NULL REFERENCES dim_customer(customer_key),
    product_key BIGINT NOT NULL REFERENCES dim_product(product_key),
    shop_key BIGINT NOT NULL REFERENCES dim_shop(shop_key),
    category_key BIGINT NOT NULL REFERENCES dim_category(category_key),
    promotion_key BIGINT NOT NULL REFERENCES dim_promotion(promotion_key),
    payment_key BIGINT NOT NULL REFERENCES dim_payment(payment_key),
    shipping_key BIGINT NOT NULL REFERENCES dim_shipping(shipping_key),
    ship_to_location_key BIGINT NOT NULL REFERENCES dim_location(location_key),
    bill_to_location_key BIGINT NOT NULL REFERENCES dim_location(location_key),

    source_order_id INTEGER NOT NULL,
    source_order_item_id INTEGER NOT NULL,
    order_number VARCHAR(60) NOT NULL,
    order_status VARCHAR(30) NOT NULL,
    payment_status VARCHAR(30) NOT NULL,

    source_customer_id INTEGER,
    source_product_id INTEGER,
    source_product_variant_id INTEGER,
    source_shop_id INTEGER,
    source_category_id INTEGER,

    currency CHAR(3) NOT NULL,
    quantity INTEGER NOT NULL CHECK (quantity > 0),
    unit_price_amount NUMERIC(12,2) NOT NULL CHECK (unit_price_amount >= 0),
    gross_sales_amount NUMERIC(12,2) NOT NULL CHECK (gross_sales_amount >= 0),
    line_discount_amount NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (line_discount_amount >= 0),
    order_discount_amount_allocated NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (order_discount_amount_allocated >= 0),
    total_discount_amount NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (total_discount_amount >= 0),
    tax_amount NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (tax_amount >= 0),
    shipping_amount_allocated NUMERIC(12,2) NOT NULL DEFAULT 0 CHECK (shipping_amount_allocated >= 0),
    net_sales_amount NUMERIC(12,2) NOT NULL CHECK (net_sales_amount >= 0),

    order_created_at TIMESTAMP NOT NULL,
    order_item_created_at TIMESTAMP NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    UNIQUE(source_order_id, source_order_item_id)
);


